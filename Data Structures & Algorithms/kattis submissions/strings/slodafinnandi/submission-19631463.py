n = int(input())
k = n // 5
res = 4*k+1 + 4*(k-k//3-(1 if k%3 == 2 else 0)) + 8*(k-1)

if k%3 == 0:
    res += 8 * ((k-6)*(k-3)//6 + 2*(k-3)//3 + (k-3)*k//6)
elif k%3 == 1:
    res += 8 * ((k-4)*(k-1)//6 + (k-4)*(k-1)//6 + (k-1)//3)
else:
    res += 8 * ((k-5)*(k-2)//6 + (k-2)//3 + (k-2)*(k-5)//6 + (k-2)*2//3)

print(res)